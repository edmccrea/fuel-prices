<section>
  <h1>About <span>Us</span></h1>
  <h2>What's the point<span>?</span></h2>
  <p>
    The aim of this site is to provide an overview of the fuel prices of the
    four major providers in Sweden. Here you can see who is consistently
    cheapest, trends in prices over time and detailed information on each
    provider.
  </p>

  <h2>How we collect the data<span>?</span></h2>
  <p>
    We take the publicly available data given by each fuel provider. The
    information isn't necessarily updated daily. An important thing to note is
    that these prices can and probably differ to the ones you find in your local
    station by a little bit.
  </p>
